use crate::{Color, Icon, IconName, IconSize};

#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub enum DxUiIcon {
    Acp,
    Agent,
    Ai,
    Automations,
    Browser,
    Channels,
    Check,
    CloudStorage,
    Commands,
    Computer,
    Connections,
    Copy,
    CreateSpace,
    Credentials,
    Duplicate,
    Evidence,
    Extensions,
    Fonts,
    Forge,
    Gateway,
    DriveProvider,
    DropboxProvider,
    Icons,
    Loading,
    Media,
    Mcp,
    Move,
    OpenFile,
    OpenProject,
    PasteInto,
    Permissions,
    Plugins,
    Project,
    Receipts,
    Search,
    Settings,
    Space,
    Source,
    Style,
    Storage,
    Ui,
    WebPreview,
}

pub fn dx_icon(icon: DxUiIcon) -> IconName {
    match icon {
        DxUiIcon::Acp => IconName::AcpRegistry,
        DxUiIcon::Agent | DxUiIcon::Ai => IconName::Sparkle,
        DxUiIcon::Automations => IconName::ListTodo,
        DxUiIcon::Browser | DxUiIcon::WebPreview => IconName::ToolWeb,
        DxUiIcon::Channels => IconName::QueueMessage,
        DxUiIcon::Check => IconName::ToolDiagnostics,
        DxUiIcon::CloudStorage => IconName::CloudDownload,
        DxUiIcon::Commands => IconName::Terminal,
        DxUiIcon::Computer => IconName::Screen,
        DxUiIcon::Connections => IconName::UserGroup,
        DxUiIcon::Copy => IconName::Copy,
        DxUiIcon::Credentials => IconName::LockOutlined,
        DxUiIcon::Duplicate => IconName::BookCopy,
        DxUiIcon::Evidence => IconName::Public,
        DxUiIcon::Extensions => IconName::BoxOpen,
        DxUiIcon::Fonts => IconName::Font,
        DxUiIcon::Forge => IconName::Forgejo,
        DxUiIcon::Gateway => IconName::Server,
        DxUiIcon::DriveProvider => IconName::DxForgeProviderDrive,
        DxUiIcon::DropboxProvider => IconName::DxForgeProviderDropbox,
        DxUiIcon::Icons => IconName::Library,
        DxUiIcon::Loading => IconName::DxLoader,
        DxUiIcon::Media => IconName::Image,
        DxUiIcon::Mcp => IconName::Server,
        DxUiIcon::Move => IconName::ArrowRightLeft,
        DxUiIcon::OpenFile => IconName::MagnifyingGlass,
        DxUiIcon::OpenProject => IconName::FolderOpenAdd,
        DxUiIcon::PasteInto => IconName::ReplyArrowRight,
        DxUiIcon::Permissions => IconName::UserCheck,
        DxUiIcon::Plugins => IconName::Blocks,
        DxUiIcon::Ui => IconName::Sliders,
        DxUiIcon::Project => IconName::FileTree,
        DxUiIcon::Receipts => IconName::FileTextOutlined,
        DxUiIcon::Search => IconName::MagnifyingGlass,
        DxUiIcon::Settings => IconName::DxCog,
        DxUiIcon::Space => IconName::Space,
        DxUiIcon::Source => IconName::FolderSearch,
        DxUiIcon::Style => IconName::Sliders,
        DxUiIcon::Storage => IconName::DatabaseZap,
        DxUiIcon::CreateSpace => IconName::CreateSpace,
    }
}

pub fn dx_loading_icon(size: IconSize, color: Color) -> Icon {
    Icon::new(dx_icon(DxUiIcon::Loading))
        .size(size)
        .color(color)
}
